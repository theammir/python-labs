# − виконує розрахунки згідно індивідуального завдання;
# − виводить результатю
# Індивідуальні завдання
# 9) Перевірити рік на високосність.

year = int(input())
if year % 4 == 0 and (year % 100 != 0 or year % 400 == 0):
    print("It's a leap year")
else:
    print("It's not a leap year")
